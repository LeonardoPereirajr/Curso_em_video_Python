num1 = int(input('Primeiro numero :  '))
num2 = int(input('Segundo numero :  '))
if num1 > num2 :
    print(' O primeiro numero , {}  é maior que o segundo  {} . '.format(num1,num2))
elif num1 < num2 :
    print(' O primeiro numero , {}  é menor que o segundo  {} . '.format(num1,num2))
elif num1 == num2:
    print(' O primeiro numero , {}  é igual ao o segundo  {} . '.format(num1, num2))